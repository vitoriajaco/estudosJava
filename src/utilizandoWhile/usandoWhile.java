package utilizandoWhile;

public class usandoWhile {

    public static void main(String[] args) {

        int carneirinho = 0;

        System.out.println("Acordado..");

        while (carneirinho <= 20) {
            System.out.println("Contando " + carneirinho + " carneirinhos ..");
            carneirinho++;
            }
        System.out.println("Dormiu");
    }
}
